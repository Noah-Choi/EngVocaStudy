package org.engvoca.domain;

public class DifficultyVO 
{
	private String diff;
	private String kor;
	private int seq;
	
	public String getDiff() 
	{
		return diff;
	}
	
	public void setDiff(String diff) 
	{
		this.diff = diff;
	}
	
	public String getKor() 
	{
		return kor;
	}
	
	public void setKor(String kor) 
	{
		this.kor = kor;
	}
	
	public int getSeq() 
	{
		return seq;
	}

	public void setSeq(int seq) 
	{
		this.seq = seq;
	}
	
}
